def addition(a:int, b:int) -> int:
    """
        Fonction qui effectue une addition
        :param a: Operande 1
        :param b: Operande 2
        :type a: int
        :type b: int
        
        :Example:
        addition(2,2)
        >>> 4
    """
    return a + b

